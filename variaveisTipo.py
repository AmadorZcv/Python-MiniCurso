#Os tipos de variaveis em python são:

umInteiro = 1          # integer(inteiro)
umDouble   = 2.143       # Float/Double(decimal)
umaString    = "Python String" # String(Sequencia de caracteres)


print (umInteiro)
print (umDouble)
print (umaString)
print(umInteiro + umDouble) #Podemos somar um inteiro e double
umInteiro = umInteiro + umDouble; #Podemos somar o valor da soma no Inteiro
print(umInteiro)
print (umInteiro + umaString)# Não podemos somar strings e valores numericos
